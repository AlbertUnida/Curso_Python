c = 0 # Asignación simple
print("Valor inicial de c:", c)
c += 10 # c = c + 1
print("Valor de c después de c += 1:", c)

c -= 1 # c = c - 1
print("Valor de c después de c -= 1:", c)

c *= 2 # c = c * 2
print("Valor de c después de c *= 2:", c)

c /= 2 # c = c / 2
print("Valor de c después de c /= 2:", c)


