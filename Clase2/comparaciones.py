r = 10 == 10 # igualdad
print("r", r)
r = 10 != 10 # desigualdad
print("r", r)
r = 10 > 5 # mayor que
print("r", r)
r = 10 < 5 # menor que
print("r", r)
r = 10 >= 10 # mayor o igual que
print("r", r)
r = 10 <= 5 # menor o igual que
print("r", r)

